public class Atribuicao {
    public static void main(String[] args) {

            //operadores de atribuicao
                // = += -= /=
        double bonus =  1800;
        bonus += 1000;
        bonus -= 1000;
        bonus *= 1000;
        bonus %= 2;
        bonus++;
        ++bonus;
        bonus++;
        System.out.println(bonus);
    }
}
